                <h1>Chiringuito Dieguichi: La Auténtica Experiencia Gastronómica de la Costa del Sol</h1>

                <p>En el corazón de Manilva, donde el sol brilla con intensidad y el mar Mediterráneo susurra sus secretos, se encuentra Chiringuito Dieguichi. Este acogedor refugio gastronómico es más que un lugar para comer, es una experiencia que transporta a los visitantes al sabor auténtico de la Costa del Sol.</p>

                <h2>Nuestra Historia</h2>
                <p>Chiringuito Dieguichi nació de la pasión por la cocina tradicional malagueña y la convicción de compartirla con todos aquellos que buscan un rincón de autenticidad en el bullicioso ritmo de las vacaciones. Nuestro dueño, Juan Diego, creció en estas costas y aprendió los secretos de la cocina local directamente de su abuela, una mujer que sabía hacer que cada plato fuera un viaje de sabores.</p>

                <h2>La Especialidad: Espetos de Sardinas</h2>
                <p>El corazón de nuestro menú es el espeto de sardinas frescas, preparado con técnicas que han sido pasadas de generación en generación. Nuestro proceso es laborioso pero respetuoso con la tradición:</p>
                <ul>
                    <li>Selección cuidadosa de las mejores sardinas frescas del día.</li>
                    <li>Preparación individual de cada espeto para asegurar la textura perfecta.</li>
                    <li>Sazonado con hierbas y especias típicamente malagueñas.</li>
                    <li>Cocinado sobre leña directamente encima del mar.</li>
                </ul>

